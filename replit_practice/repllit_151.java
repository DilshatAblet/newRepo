package replit_practice;

import java.util.Scanner;

public class repllit_151 {
    public static void main(String[] args) {
        Scanner inp = new Scanner(System.in);
        int n = inp.nextInt();

        if(n>0){
            System.out.println(1);
        }else if(n<0){
            System.out.println(-1);
        }else if(n==0){
            System.out.println(0);
        }



    }
}