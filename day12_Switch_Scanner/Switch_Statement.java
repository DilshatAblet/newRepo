package day12_Switch_Scanner;

public class Switch_Statement {
    public static void main(String[] args) {

        switch(10){
            case 5:
                System.out.println(5);
                break;
            case 1:
                System.out.println(1);
                break;
            default:
                System.out.println("invalid case");
                break;
        }





    }
}
