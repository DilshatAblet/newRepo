package day22_Nested_Loop;

public class nested_Loop {
    public static void main(String[] args) {
        for(int j = 1; j <= 5 ; j++){

            for(int i = 1; i <= 5; i++){
                System.out.print(i+" ");
            }

            System.out.println();

        }

        System.out.println("====================================================");


    }
}
